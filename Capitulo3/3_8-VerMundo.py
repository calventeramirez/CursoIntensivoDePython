lugares = ["Japon", "Alemania", "Reino Unido", "Italia"]
#Lista original
print(lugares)
#Ordenar lista sin modificar la original
print(sorted(lugares))
#Lista original
print(lugares)
#Orndenar la lista de manera inversacon sorted
print(sorted(lugares, reverse=True))
#Lista original
print(lugares)
#Lista con reverse
lugares.reverse()
print(lugares)
#La vuelvo a dejar igual
lugares.reverse()
print(lugares)
#Lista ordenada permanentemente
lugares.sort()
print(lugares)
#Lista ordenada permanentemente con reverse
lugares.sort(reverse=True)
print(lugares)